export async function POST(request: Request) {
  try {
    const body = await request.json();
    const payload = validatePayload(body);

    const zip = new JSZip();

    zip.file('package.json', genPackageJson(payload));
    zip.file('tsconfig.json', TSCONFIG);
    zip.file('.env.example', ENV_EXAMPLE);
    zip.file('README.md', README_MD);
    
    zip.file('src/config.ts', genConfigTs(payload));
    zip.file('src/utils.ts', UTILS_TS);
    zip.file('src/monitor.ts', genMonitorTs(payload));
    zip.file('src/math.ts', genMathTs(payload));
    zip.file('src/executor.ts', genExecutorTs(payload));
    zip.file('src/index.ts', genIndexTs(payload));

    const zipBuffer = await zip.generateAsync({ type: 'arraybuffer', compression: 'DEFLATE', compressionOptions: { level: 9 } });

    return new Response(zipBuffer as unknown as BodyInit, {
      status: 200,
      headers: {
        'Content-Type': 'application/zip',
        'Content-Disposition': 'attachment; filename="arbitrator-bot.zip"',
        'X-Content-Type-Options': 'nosniff',
      },
    });
  } catch (err) {
    const msg = err instanceof Error ? err.message : 'Internal Server Error';
    return new Response(msg, { status: 400, headers: { 'X-Content-Type-Options': 'nosniff' } });
  }
}
