function genExecutorTs(p: ExportPayload): string {
  const arb = p.config as ArbitrageConfig;
  const useJito = arb.executionSpeed === 'jito';
  return `import {
  VersionedTransaction, TransactionMessage, ComputeBudgetProgram,
  SystemProgram, PublicKey, AddressLookupTableAccount,
  TransactionInstruction,
} from '@solana/web3.js';
import { 
  createSyncNativeInstruction, 
  createAssociatedTokenAccountIdempotentInstruction, 
  createCloseAccountInstruction, 
  getAssociatedTokenAddressSync,
  NATIVE_MINT
} from '@solana/spl-token';
import { connection, BOT, LIMITS, loadWallet } from './config';
import { log, retryWithBackoff } from './utils';

interface ArbOpportunity { buyQuote: any; sellQuote: any; profitUsdc: number; priceImpact: number; }

function deserializeIx(ix: any): TransactionInstruction {
  return new TransactionInstruction({
    programId: new PublicKey(ix.programId),
    keys: ix.accounts.map((a: any) => ({ pubkey: new PublicKey(a.pubkey), isSigner: a.isSigner, isWritable: a.isWritable })),
    data: Buffer.from(ix.data, 'base64'),
  });
}

async function getDynamicPriorityFee(accounts: PublicKey[]): Promise<number> {
  try {
    const fees = await connection.getRecentPrioritizationFees({ lockedWritableAccounts: accounts.slice(0, 5) });
    if (!fees.length) return 50_000;
    const sorted = fees.map(f => f.prioritizationFee).sort((a, b) => a - b);
    const median = sorted[Math.floor(sorted.length / 2)];
    return Math.max(median, 1_000);
  } catch { return 50_000; }
}

function validateTxSize(tx: VersionedTransaction): boolean {
  const size = tx.serialize().length;
  if (size > LIMITS.TX_SIZE_LIMIT) {
    log('EXEC', \`TX too large: \${size} bytes\`);
    return false;
  }
  return true;
}

export async function executeTrade(opportunity: ArbOpportunity): Promise<void> {
  const wallet = loadWallet();
  const owner = wallet.publicKey;
  log('EXEC', \`Wallet: \${owner.toBase58().slice(0, 8)}... | Target Profit: $\${opportunity.profitUsdc.toFixed(4)}\`);

  // ─── Jupiter Swap Configuration ───
  // We explicitly tell Jupiter NOT to wrap/unwrap SOL automatically.
  // Why? Because we are stitching two separate swaps (Leg 1 & Leg 2) together into a single atomic transaction.
  // If Jupiter wrapped and unwrapped SOL for each leg, the WSOL account wouldn't persist between them.
  // Instead, we manage the WSOL lifecycle manually across the entire transaction.
  const swapConfig = {
    userPublicKey: owner.toBase58(),
    wrapAndUnwrapSol: false,
    dynamicComputeUnitLimit: true,
    prioritizationFeeLamports: 'auto',
  };

  const jupHeaders: Record<string, string> = { 'Content-Type': 'application/json' };
  if (BOT.JUPITER_API_KEY) jupHeaders['x-api-key'] = BOT.JUPITER_API_KEY;

  // 1. Fetch exact transaction instructions for Leg 1 (Input Token -> USDC)
  const buyRes = await fetch(\`\${BOT.JUPITER_API}/swap-instructions\`, {
    method: 'POST', headers: jupHeaders,
    body: JSON.stringify({ quoteResponse: opportunity.buyQuote, ...swapConfig }),
  });
  if (!buyRes.ok) throw new Error(\`Leg 1 Swap instructions failed: \${buyRes.status}\`);
  const buySwap = await buyRes.json();
  if (buySwap.error) throw new Error(\`Jupiter Leg 1: \${buySwap.error}\`);

  // 2. Fetch exact transaction instructions for Leg 2 (USDC -> Input Token)
  const sellRes = await fetch(\`\${BOT.JUPITER_API}/swap-instructions\`, {
    method: 'POST', headers: jupHeaders,
    body: JSON.stringify({ quoteResponse: opportunity.sellQuote, ...swapConfig }),
  });
  if (!sellRes.ok) throw new Error(\`Leg 2 Swap instructions failed: \${sellRes.status}\`);
  const sellSwap = await sellRes.json();
  if (sellSwap.error) throw new Error(\`Jupiter Leg 2: \${sellSwap.error}\`);

  // ─── Address Lookup Table (ALT) Deduplication ───
  // Solana transactions have a strict 1232 byte size limit.
  // Since we are combining two complex swaps, the transaction can easily exceed this limit.
  // We extract ALTs from both swap legs, deduplicate them, and fetch the table accounts.
  const lookupTablesMap = new Map<string, AddressLookupTableAccount>();
  const allAlts = [...(buySwap.addressLookupTableAddresses || []), ...(sellSwap.addressLookupTableAddresses || [])];
  
  for (const addr of allAlts) {
    if (!lookupTablesMap.has(addr)) {
      const result = await connection.getAddressLookupTable(new PublicKey(addr));
      if (result.value) lookupTablesMap.set(addr, result.value);
    }
  }
  const lookupTables = Array.from(lookupTablesMap.values());

  // ─── Manual WSOL Lifecycle Management ───
  // To trade SOL natively through smart contracts, it must be Wrapped SOL (WSOL).
  const wsolAta = getAssociatedTokenAddressSync(NATIVE_MINT, owner);
  
  const ixs: TransactionInstruction[] = [];

  // Step A: Create the WSOL token account (if it doesn't exist) and fund it with our trade amount

  ixs.push(createAssociatedTokenAccountIdempotentInstruction(owner, wsolAta, owner, NATIVE_MINT));
  ixs.push(SystemProgram.transfer({ fromPubkey: owner, toPubkey: wsolAta, lamports: BOT.TRADE_AMOUNT_LAMPORTS }));
  ixs.push(createSyncNativeInstruction(wsolAta));

  // Leg 1 (WSOL -> USDC)
  if (buySwap.setupInstructions) ixs.push(...buySwap.setupInstructions.map(deserializeIx));
  ixs.push(deserializeIx(buySwap.swapInstruction));
  if (buySwap.cleanupInstruction) ixs.push(deserializeIx(buySwap.cleanupInstruction)); // e.g., closing temporary non-WSOL ATAs

  // Leg 2 (USDC -> WSOL)
  if (sellSwap.setupInstructions) ixs.push(...sellSwap.setupInstructions.map(deserializeIx));
  ixs.push(deserializeIx(sellSwap.swapInstruction));
  if (sellSwap.cleanupInstruction) ixs.push(deserializeIx(sellSwap.cleanupInstruction));

  // Teardown WSOL account (unwraps all WSOL back to SOL)
  ixs.push(createCloseAccountInstruction(wsolAta, owner, owner));

  ${useJito ? `// ─── Jito Bundle ───
  const JITO_TIPS = [
    '96gYZGLnJYVFmbjzopPSU6QiEV5fGqZNyN9nmNhvrZU5',
    'HFqU5x63VTqvQss8hp11i4bVqkfRtk6rz6jZUem2wFF8',
    'Cw8CFyM9FkoMi7K7Crf6HNQqf4uEMzpKw6QNghXLvLkY',
    'ADaUMid9yfUytqMBgopwjb2DTLSLBVCzM0VKL1aYnKbn',
    'DfXygSm4jCyNCybVYYK6DwvWqjKee8pbDmJGcLWNDXjh',
  ];
  const tipAccount = new PublicKey(JITO_TIPS[Math.floor(Math.random() * JITO_TIPS.length)]);
  ixs.push(SystemProgram.transfer({ fromPubkey: owner, toPubkey: tipAccount, lamports: BOT.JITO_TIP_LAMPORTS }));
  
  // Set generous compute unit limit since Jito charges via tip
  ixs.unshift(ComputeBudgetProgram.setComputeUnitLimit({ units: LIMITS.MAX_CU }));
  ` : `// ─── Standard Priority Fees ───
  const writableAccounts = ixs.flatMap(ix => ix.keys.filter(k => k.isWritable).map(k => k.pubkey));
  const priorityFee = await getDynamicPriorityFee(writableAccounts);
  ixs.unshift(
    ComputeBudgetProgram.setComputeUnitLimit({ units: LIMITS.MAX_CU }),
    ComputeBudgetProgram.setComputeUnitPrice({ microLamports: priorityFee })
  );`}

  // ─── Build, validate size, send with retry ───
  await retryWithBackoff(async () => {
    const { blockhash, lastValidBlockHeight } = await connection.getLatestBlockhash('confirmed');
    const msg = new TransactionMessage({
      payerKey: owner, recentBlockhash: blockhash, instructions: ixs,
    }).compileToV0Message(lookupTables);
    const tx = new VersionedTransaction(msg);
    tx.sign([wallet]);

    if (!validateTxSize(tx)) throw new Error('Transaction exceeds 1232 byte limit');

    ${useJito ? `const jitoUrl = process.env.JITO_BLOCK_ENGINE_URL || 'https://mainnet.block-engine.jito.wtf';
    log('EXEC', \`Sending Jito bundle to \${jitoUrl}...\`);
    const bundleRes = await fetch(\`\${jitoUrl}/api/v1/bundles\`, {
      method: 'POST', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ jsonrpc: '2.0', id: 1, method: 'sendBundle', params: [[Buffer.from(tx.serialize()).toString('base64')]] }),
    });
    const result = await bundleRes.json();
    log('EXEC', \`Jito result: \${JSON.stringify(result)}\`);` : `log('EXEC', 'Sending via standard RPC...');
    const sig = await connection.sendRawTransaction(tx.serialize(), { skipPreflight: false, maxRetries: 3 });
    log('EXEC', \`Sent: \${sig}\`);

    const confirm = await connection.confirmTransaction({ signature: sig, blockhash, lastValidBlockHeight }, 'confirmed');
    if (confirm.value.err) throw new Error(\`TX failed: \${JSON.stringify(confirm.value.err)}\`);
    log('EXEC', 'Confirmed!');`}
  }, LIMITS.MAX_RETRIES, LIMITS.BLOCKHASH_RETRY_DELAY_MS);
}
`;
}

