// ── Validation ──
const ALLOWED_PAIRS = ['SOL/USDC', 'ETH/USDC', 'BTC/USDC'];
const ALLOWED_EXCHANGES = ['jupiter', 'raydium', 'orca'];
const ALLOWED_SPEEDS = ['standard', 'jito'] as const;

function validatePayload(body: unknown): ExportPayload {
  if (!body || typeof body !== 'object') throw new Error('Invalid payload');
  const obj = body as Record<string, unknown>;
  const config = obj.config;
  if (!config || typeof config !== 'object') throw new Error('Invalid config');
  const cfg = config as Record<string, unknown>;

  if (!ALLOWED_PAIRS.includes(cfg.tradingPair as string)) throw new Error('Invalid tradingPair');
  if (!Array.isArray(cfg.exchanges) || !cfg.exchanges.every((e: unknown) => ALLOWED_EXCHANGES.includes(e as string))) throw new Error('Invalid exchanges');
  const minProfit = Number(cfg.minProfitUsdc);
  if (!Number.isFinite(minProfit) || minProfit < 0.5 || minProfit > 10000) throw new Error('Invalid minProfitUsdc');
  const maxSize = Number(cfg.maxTradeSizeSol);
  if (!Number.isFinite(maxSize) || maxSize < 0.1 || maxSize > 100) throw new Error('Invalid maxTradeSizeSol');
  if (!ALLOWED_SPEEDS.includes(cfg.executionSpeed as typeof ALLOWED_SPEEDS[number])) throw new Error('Invalid executionSpeed');
  const tip = Number(cfg.jitoTipSol);
  if (!Number.isFinite(tip) || tip < 0 || tip > 10) throw new Error('Invalid jitoTipSol');
  return { config: { tradingPair: cfg.tradingPair as string, exchanges: cfg.exchanges as string[], minProfitUsdc: minProfit, maxTradeSizeSol: maxSize, executionSpeed: cfg.executionSpeed as ArbitrageConfig['executionSpeed'], jitoTipSol: tip } };
}

