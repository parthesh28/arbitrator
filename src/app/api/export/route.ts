  const solPrice = (Number(opp.buyQuote.outAmount) / 1e6) / BOT.TRADE_AMOUNT_SOL;
  const jitoTipUsdc = BOT.USE_JITO ? (BOT.JITO_TIP_LAMPORTS / 1e9) * solPrice : 0;

